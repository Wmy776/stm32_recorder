/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();
  
  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_FATFS_Init();
  MX_RTC_Init();
  
  MX_DMA_Init();
  MX_USART1_UART_Init();

  /* Infinite loop */
  while (1)
  {
    static uint8_t    pwmset;
    static uint16_t   time;
    static uint8_t    timeflag;
    static uint8_t    timecount;
    static GPIO_PinState    usbDetectRes;

    usbDetectRes = HAL_GPIO_ReadPin(USBDetct_GPIO_Port,USBDetct_Pin);

    if (usbDetectRes == GPIO_PIN_RESET)
    {
      if (UartHandle.UsbDetectFlag == 0)
      {
        UartHandle.UsbDetectFlag = 1;
        HAL_UART_MspDeInit(&huart1);
        MX_USB_DEVICE_Init();
      }
    }
    
    /* ºôÎüµÆ */
    if(timeflag == 0)
    {
      time ++;
      if(time >= 1600) 
        timeflag = 1;
    }
    else
    {
      time --;
      if(time == 0) 
        timeflag = 0;
    }

    /* Õ¼¿Õ±ÈÉèÖÃ */
    pwmset = time/80;

    /* 20ms Âö¿í */
    if(timecount > 20) 
      timecount = 0;
    else 
      timecount ++;

    if(timecount >= pwmset ) 
      HAL_GPIO_WritePin(C13_GPIO_Port,C13_Pin,GPIO_PIN_SET);
    else 
      HAL_GPIO_WritePin(C13_GPIO_Port,C13_Pin,GPIO_PIN_RESET);

    HAL_Delay(0); // 1ms
  }
}

